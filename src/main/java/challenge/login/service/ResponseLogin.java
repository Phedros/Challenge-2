package challenge.login.service;

public class ResponseLogin {
	
	private Boolean result;

	
	public ResponseLogin() {
		super();
	}

	public ResponseLogin(Boolean result) {
		super();
		this.result = result;
	}

	public Boolean getResult() {
		return result;
	}

	public void setResult(Boolean result) {
		this.result = result;
	}
	
	

}
